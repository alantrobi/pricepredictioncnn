import pandas as pd
import os

# ----------------------------
# Setup folders
# ----------------------------
os.makedirs("data/processed", exist_ok=True)

# ----------------------------
# Common Date Range (include 2019 anchor)
# ----------------------------
dates = pd.date_range("2018-12-31", "2023-12-31", freq="D")

# ----------------------------
# Load shared data
# ----------------------------
sensex = pd.read_csv("data/sensex.csv", index_col="Date", parse_dates=True)
usd = pd.read_csv("data/usd_inr.csv", index_col="Date", parse_dates=True)

sensex = sensex.reindex(dates).interpolate().bfill()
usd = usd.reindex(dates).interpolate().bfill()

# ----------------------------
# Function to prepare dataset
# ----------------------------
def prepare_company(stock_file, rev_data, profit_data, output_name):

    # Load stock price
    price = pd.read_csv(stock_file, index_col="Date", parse_dates=True)
    price = price.reindex(dates).interpolate().bfill()

    # Convert revenue & profit
    rev = pd.Series(rev_data)
    profit = pd.Series(profit_data)

    rev.index = pd.to_datetime(rev.index)
    profit.index = pd.to_datetime(profit.index)

    # Interpolate quarterly → daily
    rev = rev.reindex(dates).interpolate().bfill()
    profit = profit.reindex(dates).interpolate().bfill()

    # Trim to required range
    price = price["2019-01-01":]
    rev = rev["2019-01-01":]
    profit = profit["2019-01-01":]
    s = sensex["2019-01-01":]
    u = usd["2019-01-01":]

    # Combine all features
    df = pd.DataFrame({
        "price": price.squeeze(),
        "revenue": rev,
        "profit": profit,
        "sensex": s.squeeze(),
        "usd_inr": u.squeeze()
    })

    # Final safety
    df = df.bfill().ffill()

    # Save
    df.to_csv(f"data/processed/{output_name}.csv")
    print(f"✅ Saved: data/processed/{output_name}.csv")


